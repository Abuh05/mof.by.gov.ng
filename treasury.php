        <h2 id = "news">State Treasury Office(STO)</h2>
        <!-- <h4>History</h4> -->
        
        <p>The Bayelsa State Treasury office is one of the three (3) arms of the   Ministry of Finance.  The Office is also known and called the Accountant General Office. This Office has been in existence since the establishment of the Ministry of Finance.

            The Office is made up of eight (6) Departments, namely
            <ol>
                <li>Treasury Services</li>
                <li>Treasury inspectorate</li>
                <li>Treasury Administration</li>
                <li>Treasury Internal Audit</li>
                <li>Ministry of Finance (MoFI)</li>
                <li>Project FInancial Management</li>
            </ol>
          </p>
          
          
        <p> <b>RESPONSIBLITIES AND DUTIES OF THE ACCOUNTANT-GENERAL</b>
        <ul>
            <li>Serve as the Chief Accounting Officer for the receipts and payments of the government of the state.</li>
            <li>Supervise the accounts of the State Ministries, Extra-ministerial offices and other arms of Government.</li>
            <li>Collate, prepare and publish statutory financial statement of the accounts.
            <li>Manage the State Government investments.</li></li>
            <li>Maintain and operate the State accounts.</li>
            <li>Conducts routine and in-depth inspection of books of accounts of MDAs and other arms of government to ensure compliance with rules, regulations and policy decisions of the State Government.</li>
            <li>Preparation of management accounts for decision making.</li>
            <li>Approve and ensure compliance with National Chart of Accounts, internal Audit guides and stock verification manual of MDAs.</li>
            <li>Investigate cases of fraud, loss of funds, assets and store items and other financial malpractices in Ministerial offices and other arms of government.</li>
            <li>Provide financial guidelines through the issuance of treasury circulars to MDAs and other arms of government to ensure strict compliance with existing control system for the collection, custody and disbursement of public funds and stores.</li>
            <li>Supervise and control the computerization of the accounting packages and modules in MDAs e.g. (SIFMIS)</li>
            <li>Formulate the accounting policy of the State Government</li>
            <li>Service public debts and loans.</li>
            <li>Organized training of Accountants and internal audit personnel in all MDAs.</li>
            <li>Ensure compliance with GAAP, IPSAS, SFTAS and implementation of SIFMIS.</li>
        </ul>
        </p>
